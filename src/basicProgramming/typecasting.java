package basicProgramming;

public class typecasting {
	
	public static void main(String[] args)
	{
		double no = 16.78997;
		
		
		int intno; 
		
		intno = (int)no;
		
		float floatno = (float)no;
		
		System.out.println("dOUBLE VALUE: "+ no);
		System.out.println("INT VALUe: " + intno);
		System.out.println("Float VALUE: " + floatno);
	}

}
